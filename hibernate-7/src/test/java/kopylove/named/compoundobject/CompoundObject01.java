package kopylove.named.compoundobject;

class CompoundObject01
{
    private int    a;
    private String b;

    CompoundObject01(int a, String b)
    {
        this.a = a;
        this.b = b;
    }

    int getA()
    {
        return a;
    }

    void setA(int a)
    {
        this.a = a;
    }

    String getB()
    {
        return b;
    }

    void setB(String b)
    {
        this.b = b;
    }
}
